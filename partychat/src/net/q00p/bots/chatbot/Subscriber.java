package net.q00p.bots.chatbot;

import net.q00p.bots.User;

public class Subscriber {
	private User user;
	private String alias;
	private String botScreenName;
	/**
	 * @return Returns the alias.
	 */
	public String getAlias() {
		return alias;
	}
	/**
	 * @param alias The alias to set.
	 */
	public void setAlias(String alias) {
		this.alias = alias;
	}
	/**
	 * @return Returns the bot screen name.
	 */
	public String getBotScreenName() {
		return botScreenName;
	}
	/**
	 * @param botScreenName The bot screen name to set.
	 */
	public void setBotScreenName(String botScreenName) {
		this.botScreenName = botScreenName;
	}
	/**
	 * @return Returns the user.
	 */
	public User getUser() {
		return user;
	}
	/**
	 * @param user The user to set.
	 */
	public void setUser(User user) {
		this.user = user;
	}

}
